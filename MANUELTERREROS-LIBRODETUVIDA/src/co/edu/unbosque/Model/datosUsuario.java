package co.edu.unbosque.Model;

public class datosUsuario {
	private int numeroUsuaro;
	private char  letraApellido;
	private String mesNaci;
	public datosUsuario() {
		numeroUsuaro = numeroUsuaro;
		letraApellido = letraApellido;
		mesNaci = mesNaci;
	}
	public int getNumeroUsuaro() {
		return numeroUsuaro;
	}
	public void setNumeroUsuaro(int numeroUsuaro) {
		this.numeroUsuaro = numeroUsuaro;
	}
	public char getLetraApellido() {
		return letraApellido;
	}
	public void setLetraApellido(char letraApellido) {
		this.letraApellido = letraApellido;
	}
	public String getMesNaci() {
		return mesNaci;
	}
	public void setMesNaci(String mesNaci) {
		this.mesNaci = mesNaci;
	}	
}

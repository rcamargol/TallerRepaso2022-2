package co.edu.unbosque.model;

public abstract class Desterrados extends Kublacan {

	protected String vivian;
	protected String colordepiel;
	
	public Desterrados() {
		super.Alma= "";
		super.Colorojos = "";
		super.Edad = 0;
		super.Estatura = "";
		super.Nombre = "";
		this.vivian = "";
		this.colordepiel = "";
	}
	public abstract String Arrastar();
	public abstract String ejercutarVenganza();
}
